# 7 - TIPOS DE DADOS NO MYSQL

# 7.1 - NÚMERICOS: Responsável por abrigar todos os tipos de dados que envolve números inteiros, com
	  -- pontos flutuantes e decimais. O campo BOOL (BOOLEAN) também
	  -- pertence a esse conjunto por armazenar o
	  -- valor 0 (falso) e 1 (verdadeiro).

# 7.2 - TEXTUAIS: Compreende os dados relacionado a cadeia de caracteres ou binários.
	  -- Campos do tipo BLOOB tem capacidade de
	  -- armazenar arquivos de diversas extensões.
	  -- Porém fazer o uso desse tipo de prática não
	  -- é recomendo exceto em casos específicos
	  -- devido ao espaço em disco que ocupa.

# 7.3 - TEMPORAIS: Conjunto de campos para que possa ser armazenado data, hora e data/hora.
	  -- Todos os campos possuem limites que devem ser obedecidos. Alguns campos
	  -- possuem variação no uso (como o campo YEAR por exemplo).
      
# 7.4 - OUTROS TIPOS: Armazena outros tipos de dados como os geométricos por exemplo (que é pouco
	  -- utilizado nos ERP’s e aplicativos mobile). Armazena também a estrutura do campo
	  -- AUTO_INCREMENT, que embora seja um campo inteiro, possuir propriedades particulares.


